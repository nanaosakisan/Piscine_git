void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long n;
	
	n = nb;
	if (n > 2147483647 || n < -2147483648)
		return (0);
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
